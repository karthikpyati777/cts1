package oops.com;

public class Students {
	
	
	private int id;
	private String name;
	private String branch;
	
	public void setId(int id) {
		this.id=id;
	}
	
	public int getId() {
		return id;
	}
	
	public void setName(String name) {
		this.name=name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setBranch(String branch) {
		this.branch=branch;
	}
	
	public String getBranch() {
		return branch;
	}

}
